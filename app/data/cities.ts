const CITY_BY_ID: Record<string, City> = Object.fromEntries(CITIES.map((c) => [c.id, c]))

export function citiesForActiveBattles(battleIds: string[]): City[] {
  const seen = new Set<string>()
  const out: City[] = []
  for (const bid of battleIds) {
    const cid = BATTLE_TO_CITY[bid]
    if (!cid || seen.has(cid)) continue
    const c = CITY_BY_ID[cid]
    if (c) {
      seen.add(cid)
      out.push(c)
    }
  }
  return out
}
